class DataType1 {
    public static void main(String[] args) {

        int num = 10;
        float f = 5.5f;
        double d = 99.99;
        char ch = 'A';
        boolean flag = true;

        System.out.println("Integer: " + num);
        System.out.println("Float: " + f);
        System.out.println("Double: " + d);
        System.out.println("Character: " + ch);
        System.out.println("Boolean: " + flag);
    }
}