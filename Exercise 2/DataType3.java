class DataType3 {
    public static void main(String[] args) {

        int a = 5;
        double b = 2.5;

        double result = a + b;   // automatic type promotion

        char grade = 'A';
        boolean pass = true;

        System.out.println("Result: " + result);
        System.out.println("Grade: " + grade);
        System.out.println("Pass: " + pass);
    }
}