class DataOp3 {
    public static void main(String[] args) {

        // Implicit Casting
        int num = 100;
        double d = num;
        System.out.println("Implicit Casting: " + d);

        // Explicit Casting
        double value = 99.99;
        int i = (int) value;
        System.out.println("Explicit Casting: " + i);
    }
}